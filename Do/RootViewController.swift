    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return dataSource.count
    }
    
    override func tableView(_ tableView: UITableView, heightForRowAt indexPath: IndexPath) -> CGFloat {
        return 44
    }

    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "cell", for: indexPath)

        cell.textLabel?.text = dataSource[indexPath.row]

        return cell
    }
    
    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        let textSelected = dataSource[indexPath.row]
        
        var viewController : UIViewController?
        
        if textSelected == "Do TableView" {
            viewController = ViewController()
        }
        else if textSelected == "Do GridView" {
            viewController = GridViewController()
        }
        else if textSelected == "CollectionView" {
            let layout = UICollectionViewFlowLayout()
            viewController = CollectionViewController(collectionViewLayout: layout)
        }
        else {
            viewController = UIViewController()
        }
        
        navigationController?.pushViewController(viewController!, animated: true)
    }

